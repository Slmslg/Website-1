		<h4>Bilgisayar Mühendisleri Nerelerde Çalışabilir ?</h4>
		<p>
	Bu konuda aslında sınır yok diyebilirim. İsterseniz Devlet kurumlarında çalışabilirsiniz. 
	Eğer yükselmek gibi bir hedefiniz varsa özel şirketlerde,holdinglerde, savunma sanayinde, yazılım üreten şirketlerde, bankalarda,TürkTelekom,ASELSAN,HAVELSAN,Microsoft,IBM,Sony,Komtaş Bilgi Yönetimi, Web Tasarım Şirketlerinde, Sistem Uzmanı, Web Tasarımcı, Programcı, Yazılım Modellemesi Uzamanı, Ağ Uzmanı, Veri Tabanı Programcısı vs. gibi önemli yerlerin önemli mevkilerinde çalışabilirsiniz.
	İsterseniz de kendi şirketinizi kurabilir işletebilirsiniz. <br>
	Çalıştığınız şirkette yönetici olabilirsiniz. Bankalarda Bilgisayar Mühendislerinin daha kolay yükseldiği söylenir. 
	Maaş konusunda da her yıl yükselen bir grafik göze çarpar. <br>Devlette 3.512 TL gibi bir maaş alırsınız. <br>
