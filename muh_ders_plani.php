<p>		<h4>Bilgisayar Mühendisliği Hakkında</h4>
<p>
		Bu yazımda yeni Bilgisayar Mühendisliğine başlamış ya da başlayacak kişilerin bilgi edinmesi için bu bölüm hakkında bilgi vereceğim.
		Yaşadığım tecrübeleri ve dersler hakkında bilgileri vereceğim. Dersleri sınıflara göre ayırıp önerilerde bulunacağım. 
		Bu anlattıklarım Düzce Üniversitesi'nden farklı üniversitelerde aynı olmayabilir fakat muhtemelen çoğu benzer olacaktır.
		Bilgisayar Mühendisliği hakkında biraz bilgi vermek istiyorum.</p>
		<p>
		Bu mühendislik dalında bilgisayar donanımı, yazılımı, robotik, elektrik, elektronik ve ağ konuları hakkında teknik ve teorik bilgiler edinizsiniz.
		Üniversiteden üniversiteye değişse de epey zorlu bir bölümdür.<br>
		Oldukça çok sayısal zeka ve mantık ister.<br> Bu yüzden bir problemi çözmek için farklı düşünmeniz gerekir.<br>
		Problem çözme yeteneğinizin iyi olması gerekir.<br>
		Farklı ve kısa yoldan fikirler ediniyor olmanız ve yaratıcı olmanız gereklidir.<br>
		En temel özelliğinizin ise kolay ve hızlı öğrenebiliyor olmanızdır.<br>
		Yabancı dil de oldukça önemli olup sıkıştığınız zaman yabancı kaynaklardan yararlanma yoluna genellikle ilk tercihiniz olması gerekiyor.<br>
		Yani bundan yola çıkarak araştırmacı ve pratik olmanız gerektiğini de anlamış oluyorsunuz. 
		Temel bilgi birikiminizin iyi olması çok önemlidir. Yapacağınız proje ve ya iş hayatının gerektirdiği iş alanını bilemezsiniz.
		Bu tıp ya da matematik, fizik gibi alanlar olacağı gibi network ya da web üzerine bir işte de çalışabilirsiniz.
		Tamamen önünüze çıkan iş olasılıklarına bağlıdır. Ama en buradaki en önemli nokta sizin mühendis olduktan sonra ne yapmak, stedğiniz.
		Eğer bu işi yapmak isterseniz sabırlı olmanız ve yaptığınız işten zevk almanız gerekir yoksa bundan sonraki hayatınız oldukça kötü ve sıkıcı geçebilir.
		Stresli ortamlara alışık olmalı ve daima yenilikçi düşünmek zorundasınız.<br>
		Bu meslekteki en önemli hususlardan biri bence yaptığınız işe saygı göstermektir.Yapabildiğiniz kadar iyi bir iş fazla emek vererek ancak kendinizi geliştirip iyi işler çıkartabilirsiniz.
		İşinize olan saygınlığınız size verilen değeri ve saygınlığınızı, güveni de arttıracaktır. 
